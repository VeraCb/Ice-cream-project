function openModal() {
  var modal = document.getElementById("myModal");
  modal.style.display = "block";
}

function closeModal() {
  var modal = document.getElementById("myModal");
  modal.style.display = "none";
}

function openModalWindow() {
  var modal = document.getElementById("myModalWindow");
  modal.style.display = "block";
}

function closeModalWindow() {
  var modal = document.getElementById("myModalWindow");
  modal.style.display = "none";
}
